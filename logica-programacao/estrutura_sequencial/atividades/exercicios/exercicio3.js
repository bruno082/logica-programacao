let numero1, numero2, soma, subtração, divisão, multiplicação

numero1 = parseInt(prompt("digite um numero"))
numero2 = parseInt(prompt("digite um segundo numero"))

soma = numero1 + numero2

document.write(soma, "<br>")

divisão = numero1 / numero2

document.write(divisão, "<br>")

multiplicação = numero1 * numero2

document.write(multiplicação, "<br>")

subtração = numero1 - numero2

document.write(subtração)

